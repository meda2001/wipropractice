num1 = int(input("Enter num1: "))
num2 = int(input("Enter num2: "))

# Arithmetic Operators


print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(num1 % num2)
print(num1 ** num2)

#Assignment Operators

num1 += 15
num1 -= 2
num1 *= 2
num1 /= 2
num1 %= 5
num1 **= 2
print(num1)

# Comparison Operators

print(num1 == num2)
print(num1 != num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)


# Logical Operators

print((num1 > num2) and (num1 > 10))
print((num1 > num2) or (num1 > 10))
print(not num1 > num2)


